# Creación de listas
mi_lista = []

# Creación de lista con valores inicializados
mi_lista = [1,2,3,4,5]

# Agregar elemento al final de la lista
mi_lista.append(6)

# Eliminamos elemento de la lista
mi_lista.remove(2)




# Mostramos mensajes:
print(mi_lista)

# Mostramos un elemento por el indice(posicion)
numero_cinco =  mi_lista[3]
print(numero_cinco)


# Mostramos la longitud de la lista
total_elementos = len(mi_lista)
print(total_elementos)

